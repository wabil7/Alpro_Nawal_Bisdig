x = 5
y = 4

print("x > y :", x > y)         #Lebih Besar
print("x < y :", x < y)         #Lebih Kecil
print("x == y :", x == y)       #Sama Dengan
print("x != y :", x != y)       #Tidak Sama Dengan
print("x >= y :", x >= y)       #Lebih Besar Sama Dengan
print("x <= y :", x <= y)       #Lebih Kecil Sama Dengan