#Menginpukan Nilai 8 di a.
a = 8

#Menambahkan dengan 2
a += 2

#Menampilkan Hasil Nilai a ditambah dengan 2.
print(a)

#Mengurangi dengan 3.
a -= 3

#Menampilkan Hasil Nilai a dikurangi dengan 3.
print(a)

#Kali 10
a *= 10

#Menampilkan Hasil Nilai a dikali dengan 10.
print(a)

#Membagi dengan 4.
a /= 4

#Menampilkan Hasil Nilai a dibagi dengan 4.
print(a)

#Mepangkatkan 2.
a **= 2

#Menampilkan Hasil Nilai a dipangkatkan dengan 2.
print(a)

#Berapkah Nilai a sekarang?
print("Nilai a adalah :", a)